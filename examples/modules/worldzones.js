function pos2zone(pos, city)
{
	if(Array.isArray(pos)) {
		pos = {x: pos[0], y: pos[1]};
	}
	for(let i = city ? 0 : 3, len = city ? 3 : zone.length; i < len; i++)
	{
		if((pos.x >= zone[i].min.x && pos.x <= zone[i].max.x) && (pos.y >= zone[i].min.y && pos.y <= zone[i].max.y)) {
			return zone[i].name;
		}
	}
	return null;
}

